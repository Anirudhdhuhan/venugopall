							Dr. Bhimrao Ramji Ambedkar, popularly known as Babasaheb Ambedkar, was a jurist, social reformer and politician. He is also known as the Father of Indian Constitution. A well-known politician and an eminent jurist, his efforts to eradicate social evils like untouchablity and caste restrictions were remarkable. Throughout his life, he fought for the rights of the dalits and other socially backward classes. Ambedkar was appointed as India’s first Law Minister in the Cabinet of Jawaharlal Nehru. He was posthumously awarded the Bharat Ratna, India’s highest civilian honor, in 1990. <br><br>

							Bhimrao Ambedkar was born to Bhimabai and Ramji on 14 April 1891 in Mhow Army Cantonment, Central Provinces (Madhya Pradesh). Ambedkar’s father was a Subedar in the Indian Army and after his retirement in 1894, the family moved to Satara, also in Central Provinces. Shortly after this, Bhimrao’s mother passed away. Four years later, his father remarried and the family shifted to Bombay. In 1906, 15 year old Bhimrao married Ramabai, a 9 year old girl. His father Ramji Sakpal died in Bombay, in 1912.<br><br>

							Throughout his childhood, Ambedkar faced the stigmas of caste discrimination. Hailing from the Hindu Mahar caste, his family was viewed as “untouchable” by the upper classes. The discrimination and humiliation haunted Ambedkar at the Army school. Fearing social outcry, the teachers would segregate the students of lower class from that of Brahmins and other upper classes. The untouchable students were often asked by the teacher to sit outside the class. After shifting to Satara, he was enrolled at a local school but the change of school did not change the fate of young Bhimrao. Discrimination followed wherever he went. After coming back from the US, Ambedkar was appointed as the Defence secretary to the King of Baroda but there also he had to face the humiliation for being an ‘Untouchable’. <br><br>

							He cleared his matriculation in 1908 from Elphinstone High School. In 1908, Ambedkar got the opportunity to study at the Elphinstone College and obtained his graduate degree in Economics and Political Science in the year 1912 from Bombay University. Besides clearing all the exams successfully Ambedkar also obtained a scholarship of twenty five rupees a month from the Gaekwad ruler of Baroda, Sahyaji Rao III. Ambedkar decided to use the money for higher studies in the USA. He enrolled in the Columbia University in New York City to study Economics. He completed his Master’s degree in June 1915 after successfully completing his thesis titled ‘Ancient Indian Commerce’. <br><br>

							In 1916, he enrolled in the London School of Economics and started working on his doctoral thesis titled “The problem of the rupee: Its origin and its solution”. With the help of the former Bombay Governor Lord Sydenham, Ambedkar became a professor of political economy at the Sydenham College of Commerce and Economics in Bombay. In order to continue his further studies, he went to England in 1920 at his own expense. There he was received the D.Sc by the London University. Ambedkar also spent a few months at the University of Bonn, Germany, to study economics. He received his PhD degree in Economics in 1927. On 8 June, 1927, he was awarded a Doctorate by the University of Columbia.<br><br>

							After returning to India, Bhimrao Ambedkar decided to fight against the caste discrimination that plagued him throughout his life. In his testimony before the Southborough Committee in preparation of the Government of India Act in 1919, Ambedkar opined that there should be separate electoral system for the Untouchables and other marginalised communities. He contemplated he idea of reservations for Dalits and other religious outcasts. <br><br>

							Ambedkar began to find ways to reach to the people and make them understand the drawbacks of the prevailing social evils. He launched a newspaper called “Mooknayaka” (leader of the silent) in 1920 with the assistance of Shahaji II, the Maharaja of Kolkapur. It is said that after hearing his speech at a rally, Shahu IV, an influential ruler of Kolhapur, dined with the leader. The incident also created a huge uproar in the socio-political arena of the country. <br><br>

							Ambedkar started his legal career after passing the Bar course in Gray’s Inn. He applied his litigious skills in advocating cases of caste discrimination. His resounding victory in defending several non-Brahmin leaders accusing the Brahmins of ruining India, established the bases of his future battles.<br><br>

							By 1927, Ambedkar launched full-fledged movements for Dalit rights. He demanded public drinking water sources open to all and right for all castes to enter temples. He openly condemned Hindu Scriptures advocating discrimination and arranged symbolic demonstrations to enter the Kalaram Temple in Nashik.
                            <br><br>
							In 1932, the Poona Pact was signed between Dr. Ambedkar and Pandit Madan Mohan Malviya, representative of the Hindu Brahmins relinquishing reservation of seats for the untouchable classes in the Provisional legislatures, within the general electorate. These classes were later designated as Scheduled Classes and Scheduled Tribes.<br><br>						 

							In 1936, Ambedkar founded the Independent Labor Party. In the 1937 elections to the Central Legislative Assembly, his party won 15 seats. Ambedkar oversaw the transformation of his political party into the All India Scheduled Castes Federation, although it performed poorly in the elections held in 1946 for the Constituent Assembly of India.<br><br>

							Ambedkar objected to the decision of the Congress and Mahatma Gandhi to call the untouchable community as Harijans. He would say that even the members of untouchable community are same as the other members of the society. Ambedkar was appointed on the Defence Advisory Committee and the Viceroy’s Executive Council as Minister for Labor. <br><br>

							His reputation as a scholar led to his appointment as free India’s first Law Minister and chairman of the committee responsible to draft a constitution for independent India.<br><br>						 

							Dr. Ambedkar was appointed as the chairman of the constitution drafting committee on August 29, 1947. Ambedkar emphasized on the construction of a virtual bridge between all classes of the society. According to him, it would be difficult to maintain the unity of the country if the difference among the classes were not met. He put particular emphasis on religious, gender and caste equality. He was successful in receiving support of the Assembly to introduce reservation for members of the 
							scheduled castes and scheduled tribes in education, government jobs and civil services.<br><br>

							In 1950, Ambedkar travelled to Sri Lanka to attend a convention of Buddhist scholars and monks. After his return he decided to write a book on Buddhism and soon, converted to Buddhism. In his speeches, Ambedkar lambasted the Hindu rituals and caste divisions. Ambedkar founded the Bharatiya Bauddha Mahasabha in 1955. His book, "The Buddha and His Dhamma" was published posthumously.<br><br>

							On October 14, 1956 Ambedkar organized a public ceremony to convert around five lakh of his supporters to Buddhism. Ambedkar traveled to Kathmandu to attend the Fourth World Buddhist Conference. He completed his final manuscript, "The Buddha or Karl Marx" on December 2, 1956.<br><br>

							Since 1954-55 Ambedkar was suffering from serious health problems including diabetes and weak eyesight. On 6 December, 1956 he died at his home in Delhi. Since, Ambedkar adopted Buddhism as his religion, a Buddhist-style cremation was organized for him. The ceremony was attended by hundreds of thousands of supporters, activists and admirers.<br>
